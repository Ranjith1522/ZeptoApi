import { createSlice } from '@reduxjs/toolkit';

const signUpSlice = createSlice({
  name: 'counter',          
  initialState: {           
    value: 0
  },
  reducers: {               
    increment: (state) => {
      state.value += 1;
    },
    decrement: (state) => {
      state.value -= 1;
    },
  }
});

export const { increment, decrement } = signUpSlice.actions;
export default signUpSlice.reducer;
